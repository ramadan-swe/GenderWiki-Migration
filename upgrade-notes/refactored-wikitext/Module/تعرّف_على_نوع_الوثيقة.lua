function p.main(frame)
	local args = frame.args
	local raw = args[1] or ""
	-- Normalize: remove shadda
	local norm = mw.ustring.gsub(raw, "ّ", "")
	local cat, cls

	-- Try image types first
	cat = imageTypes[norm]
	if cat then
		cls = "صورة"
		return cat .. "␟" .. cls
	end

	-- Try space-normalized: replace "قصة مصورة" with "قصة مصوّرة"
	-- (some entries have and some don't have shadda on the و)
	local norm2 = mw.ustring.gsub(norm, "مصورة", "مصوّرة")
	norm2 = mw.ustring.gsub(norm2, "مذكرات", "مذكّرات")
	norm2 = mw.ustring.gsub(norm2, "مطوية", "مطويّة")
	cat = docTypes[norm] or docTypes[norm2]

	if cat then
		cls = "وثيقة"
		return cat .. "␟" .. cls
	end

	-- Unknown: treat as doc
	return "وثائق فضائية␟وثيقة"
end

return p
